package org.jpy.dummies;

/**
 * @author Norman Fomferra
 */
public class Thing {
    private int value;

    public Thing() {
    }

    public Thing(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
